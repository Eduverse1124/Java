// Java Object Oriented Programming Code 

public class Ayush {
    
    // instance variable
    public int a = 1124;

    // instance method 
    public void ayu() {
        System.out.println("Magic Number is : " + a);
    }

    public static void main(String[] args) {
        Ayush obj = new Ayush();
        System.out.println("=== Main Start ===");
        obj.ayu();
    }
}

